  static List<Question> getQuestionsForChapter(String chapterId) {
    return questions.where((q) => q.chapterId == chapterId).toList();
  }

  static int get totalQuestions => questions.length;

  static int get totalChapters => chapters.length;

  static Chapter? getChapterById(String chapterId) {
    try {
      return chapters.firstWhere((c) => c.id == chapterId);
    } catch (_) {
      return null;
    }
  }

  static List<Question> getQuestionsByDifficulty(Difficulty difficulty) {
    return questions.where((q) => q.difficulty == difficulty).toList();
  }

  static List<Question> getQuestionsByPriority(QuestionPriority priority) {
    return questions.where((q) => q.priority == priority).toList();
  }

  static List<Question> searchQuestions(String query) {
    final lowerQuery = query.toLowerCase();
    return questions.where((q) {
      return q.question.toLowerCase().contains(lowerQuery) ||
          q.purpose.toLowerCase().contains(lowerQuery) ||
          q.searchTags.any((tag) => tag.toLowerCase().contains(lowerQuery));
    }).toList();
  }
