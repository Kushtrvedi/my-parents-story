  static String getLocalizedChapterTitle(Chapter chapter, String localeCode) {
    final langMap = _chapterTitles[localeCode];
    if (langMap != null && langMap.containsKey(chapter.id)) {
      return langMap[chapter.id]!;
    }
    return chapter.title;
  }

  static String getLocalizedQuestion(Question question, String localeCode) {
    if (localeCode == 'en') return question.question;

    final langMap = _questionTranslations[localeCode];
    if (langMap != null) {
      if (langMap.containsKey(question.id)) {
        return langMap[question.id]!;
      }

      final normalizedId = question.id.replaceAll('ch', 'q').replaceAll('_', '');
      if (langMap.containsKey(normalizedId)) {
        return langMap[normalizedId]!;
      }

      final chNum = question.chapterNumber.toString().padLeft(2, '0');
      final qNum = question.questionNumber.toString().padLeft(2, '0');
      final altId = 'ch${chNum}_q$qNum';
      final altShortId = 'q$chNum$qNum';

      if (langMap.containsKey(altId)) return langMap[altId]!;
      if (langMap.containsKey(altShortId)) return langMap[altShortId]!;
    }

    if (localeCode == 'hi') {
      return _generateHindiQuestionFallback(question);
    }
    if (localeCode == 'gu') {
      return _generateGujaratiQuestionFallback(question);
    }
    if (localeCode == 'es') {
      return _generateSpanishQuestionFallback(question);
    }

    return question.question;
  }

